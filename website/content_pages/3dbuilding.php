        <h2>How to Build a 3D Printer From Scratch</h2>

        <br/>

        <img src="images/tutorial11.jpg" width="80%">

        <div class="text_wrapper">
            <p>If you are reading this article that means that you’ve probably decided to join the community of 3d printing
                enthusiasts and find out more about 3d printers, or maybe you have already got some experience in it and are
                ready to try to create your own 3d printer from scratch.</p>

            <p>In this article you will find general understanding how 3D printers can be built as well as links to really
                useful resources where you can find more detailed  information with step-by-step description.</p>

            <p>Currently there is a big variety of <b>3d printers</b> in the Internet that already assembled and are ready to
                print right after you unpack them from a box, of course you also need to buy a
                <a href="http://3dprintingfromscratch.com/3d-printer-filament-finder/">3d printer</a> filament for it.
                Do not forget that not all 3d printers can use the same filament types, so please read the instruction
                before you purchase it.</p>
        </div>

        <img src="images/tutorial12.jpg" width="80%">

        <div class="text_wrapper">
            <p>But for some people who love to make something new with their own hands can be more interesting to build
                their own 3d printer from scratch. Based on our research the 3D printing community is filled with many
                enthusiastic people so called DIY’ers (Do It Yourself’ers), who are fascinated with building 3d printers
                literally almost from scratch.</p>
        </div>

        <img src="images/tutorial13.jpg" width="80%">

        <div class="text_wrapper">
            <p>Good example of it is RepRap movement. RepRap is short for a <b>Rep</b>licating <b>Rap</b>id-prototyper.
                In other words it is the self-replicating 3d printer. In fact most of today’s top consumer level 3D
                printers (picture above) are originated in one way or another from RepRap.</p>
        </div>

        <iframe src="https://player.vimeo.com/video/5202148" width="640" height="512" frameborder="0"
                webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>

        <div class="text_wrapper">
            <p>You can find a lot of examples on how to build a 3d printer from scratch on
                <a href="http://reprap.org/">RepRap website</a>, but we also
                want to describe some of more essential steps in order to give an overall understanding of what to expect
                if you really decide to make your own 3d printer.</p>

            <p>The first step is to find and buy 3d printer DIY kit. Kits usually contain most of parts needed to
                assemble the printer and can be divided into two main types:</p>

            <p><b>Scratch Built 3D Printer</b> – This 3d printer DIY kit is for those who prefer to build thing from scratch,
                or in other words go to DIY RepRap route. Apart from some essential advantages this approach has also
                its disadvantages. In theory it can save you some money and since you build it from scratch you will
                know every nut and bolt in it by the time you can use it and successfully print your first object. Main
                disadvantage of this method is time. Literally it will take a lot of time to deal with all these hundreds
                of bolts, wires, belts and much other stuff. In the end you still need to select and purchase or build
                a controller board, but it depends on your skills level.</p>
        </div>

        <img src="images/tutorial14.jpg" width="80%">

        <div class="text_wrapper">
            <p><b>Kit Built 3D Printer</b> – This type of kit is good if you want to save some money and at the same time not
                to deal with all challenges described in the 1st kit type. Many manufactures sell such kits with
                “easy-to-assemble” instructions included. I doubt that it will be as easy as assemble a chair from IKEA,
                so you still have to be mechanically inclined and able to understand basic geometry. Some of such kits
                contain not all parts and you may need to buy something in addition.</p>
            <p>The next step will be to determine the design for your 3D printer. In my humble opinion most of DIY 3D
                printers have not very breathtaking and futuristic design in comparison with MakerBot and other brands.
                But it is also a matter of taste, for some people the design is not very important.</p>
        </div>

        <img src="images/tutorial15.jpg" width="80%">

        <div class="text_wrapper">
            <p>Once you have got your 3d print DIY kit and chosen the design you could go ahead and start the most
                fascinating and time consuming part – build your own 3d printer. Be prepared for sleepless nights and a
                lot of fun. Maybe you will make up something new and present your findings to RepRap community.</p>
            <p>When the hardware part of the 3d printer is ready you also need to add a software component to it. It
                basically does the following things: allows you to view and alter 3d graphical objects, as well as
                converts the 3d images into instructions that 3d printer can understand.</p>
        </div>

        <img src="images/tutorial16.jpg" width="80%">

        <div class="text_wrapper">
            <p>Finally you can 3d print your first object! If you do not have anything to print, you can find a big
                variety of objects on specialized marketplaces such as <a href="http://www.shapeways.com/">Shapeways</a>,
                <a href="https://www.thingiverse.com/">Thingiverse</a>> and others. Before printing make sure that
                the object you want to 3d print has the right size and fit on the printer frame.</p>
